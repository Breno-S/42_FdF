char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char *)0;
	while (*s)
	{
		if (*s == (char)c)
			ptr = (char *)s;
		s++;
	}
	if (*s == (char)c)
		ptr = (char *)s;
	return (ptr);
}
