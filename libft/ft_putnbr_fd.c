#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;
	char	digit;

	nbr = n;
	digit = '0';
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr *= -1;
	}
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	if (nbr < 10)
	{
		digit += (nbr % 10);
		write(fd, &digit, 1);
	}
}
